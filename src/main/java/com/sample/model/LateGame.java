package com.sample.model;

public class LateGame {
	private boolean teamKills;
	private boolean teamTowers;
	private boolean teamObjectives;
	public boolean isTeamKills() {
		return teamKills;
	}
	public void setTeamKills(boolean teamKills) {
		this.teamKills = teamKills;
	}
	public boolean isTeamTowers() {
		return teamTowers;
	}
	public void setTeamTowers(boolean teamTowers) {
		this.teamTowers = teamTowers;
	}
	public boolean isTeamObjectives() {
		return teamObjectives;
	}
	public void setTeamObjectives(boolean teamObjectives) {
		this.teamObjectives = teamObjectives;
	}
	
	

}
