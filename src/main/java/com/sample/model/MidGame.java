package com.sample.model;

public class MidGame {
	private String lane;
	private int allyKills;
	private int enemyKills;
	private int allyTowers;
	private int enemyTowers;
	private int allyObjectives;
	private int enemyObjectives;
	public String getLane() {
		return lane;
	}
	public void setLane(String lane) {
		this.lane = lane;
	}
	public int getAllyKills() {
		return allyKills;
	}
	public void setAllyKills(int allyKills) {
		this.allyKills = allyKills;
	}
	public int getEnemyKills() {
		return enemyKills;
	}
	public void setEnemyKills(int enemyKills) {
		this.enemyKills = enemyKills;
	}
	public int getAllyTowers() {
		return allyTowers;
	}
	public void setAllyTowers(int allyTowers) {
		this.allyTowers = allyTowers;
	}
	public int getEnemyTowers() {
		return enemyTowers;
	}
	public void setEnemyTowers(int enemyTowers) {
		this.enemyTowers = enemyTowers;
	}
	public int getAllyObjectives() {
		return allyObjectives;
	}
	public void setAllyObjectives(int allyObjectives) {
		this.allyObjectives = allyObjectives;
	}
	public int getEnemyObjectives() {
		return enemyObjectives;
	}
	public void setEnemyObjectives(int enemyObjectives) {
		this.enemyObjectives = enemyObjectives;
	}
	
	
	

}
